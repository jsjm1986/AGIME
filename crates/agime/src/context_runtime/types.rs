#[derive(Debug, Clone, Copy, PartialEq)]
pub(crate) struct RuntimeBudget {
    pub context_limit: usize,
    pub effective_context_limit: usize,
    pub projected_tokens: usize,
    pub output_reserve_tokens: usize,
    pub auto_compact_threshold: f64,
}

impl RuntimeBudget {
    pub(crate) fn new(
        context_limit: usize,
        projected_tokens: usize,
        output_reserve_tokens: usize,
        auto_compact_threshold: f64,
    ) -> Self {
        let effective_context_limit = context_limit.saturating_sub(output_reserve_tokens);
        Self {
            context_limit: context_limit.max(1),
            effective_context_limit: effective_context_limit.max(1),
            projected_tokens,
            output_reserve_tokens,
            auto_compact_threshold,
        }
    }

    pub(crate) fn usage_ratio(self) -> f64 {
        self.projected_tokens as f64 / self.context_limit as f64
    }

    pub(crate) fn effective_usage_ratio(self) -> f64 {
        self.projected_tokens as f64 / self.effective_context_limit as f64
    }

    pub(crate) fn remaining_headroom(self) -> usize {
        self.effective_context_limit
            .saturating_sub(self.projected_tokens)
    }

    pub(crate) fn auto_compact_threshold_tokens(self, threshold_override: Option<f64>) -> usize {
        let ratio_threshold = threshold_override.unwrap_or(self.auto_compact_threshold);
        let ratio_tokens = (self.context_limit as f64 * ratio_threshold)
            .round()
            .clamp(0.0, self.context_limit as f64) as usize;
        let headroom_tokens = self
            .effective_context_limit
            .saturating_sub(AUTO_HEADROOM_BUFFER_TOKENS);
        ratio_tokens.min(headroom_tokens)
    }

    pub(crate) fn should_commit_staged_collapse(self) -> bool {
        self.projected_tokens >= COMMIT_ABSOLUTE_TRIGGER_TOKENS
            || self.remaining_headroom() <= COMMIT_HEADROOM_BUFFER_TOKENS
    }

    pub(crate) fn should_auto_session_memory(self, threshold_override: Option<f64>) -> bool {
        self.projected_tokens >= AUTO_ABSOLUTE_TRIGGER_TOKENS
            || self.remaining_headroom() <= AUTO_HEADROOM_BUFFER_TOKENS
            || self.projected_tokens >= self.auto_compact_threshold_tokens(threshold_override)
    }

    pub(crate) fn should_stage(self) -> bool {
        self.projected_tokens >= STAGE_ABSOLUTE_TRIGGER_TOKENS
            || self.remaining_headroom() <= STAGE_HEADROOM_BUFFER_TOKENS
            || self.effective_usage_ratio() >= DEFAULT_STAGE_THRESHOLD
    }
}

