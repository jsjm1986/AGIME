pub(crate) fn rebuild_store_from_entry_log(store: ContextRuntimeStore) -> ContextRuntimeStore {
    let entry_log = store.entry_log.clone();
    let mut rebuilt = ContextRuntimeStore {
        entry_log,
        collapse_commits: Vec::new(),
        staged_snapshot: None,
        session_memory: None,
        snip_entries: Vec::new(),
        microcompact_entries: Vec::new(),
        preserved_segment: None,
        boundary: None,
    };

    for entry in rebuilt.entry_log.clone() {
        match entry {
            ContextRuntimeStoreEntry::StagedSnapshotSet { snapshot } => {
                rebuilt.staged_snapshot = Some(snapshot);
            }
            ContextRuntimeStoreEntry::StagedSnapshotCleared => {
                rebuilt.staged_snapshot = None;
            }
            ContextRuntimeStoreEntry::CollapseCommitAdded { commit } => {
                rebuilt.collapse_commits.push(commit);
            }
            ContextRuntimeStoreEntry::CollapseCommitsCleared => {
                rebuilt.collapse_commits.clear();
            }
            ContextRuntimeStoreEntry::SessionMemorySet { session_memory } => {
                rebuilt.session_memory = Some(session_memory);
            }
            ContextRuntimeStoreEntry::SessionMemoryCleared => {
                rebuilt.session_memory = None;
            }
            ContextRuntimeStoreEntry::SnipEntriesSet { entries } => {
                rebuilt.snip_entries = entries;
            }
            ContextRuntimeStoreEntry::SnipEntriesCleared => {
                rebuilt.snip_entries.clear();
            }
            ContextRuntimeStoreEntry::MicrocompactEntriesSet { entries } => {
                rebuilt.microcompact_entries = entries;
            }
            ContextRuntimeStoreEntry::MicrocompactEntriesCleared => {
                rebuilt.microcompact_entries.clear();
            }
            ContextRuntimeStoreEntry::BoundarySet { boundary } => {
                rebuilt.boundary = Some(boundary);
            }
            ContextRuntimeStoreEntry::BoundaryCleared => {
                rebuilt.boundary = None;
            }
            ContextRuntimeStoreEntry::PreservedSegmentSet { preserved_segment } => {
                rebuilt.preserved_segment = Some(preserved_segment);
            }
            ContextRuntimeStoreEntry::PreservedSegmentCleared => {
                rebuilt.preserved_segment = None;
            }
        }
    }

    if !entry_log_mentions_boundary(&rebuilt.entry_log) {
        rebuilt.boundary = store.boundary;
    }
    if !entry_log_mentions_preserved_segment(&rebuilt.entry_log) {
        rebuilt.preserved_segment = store.preserved_segment;
    }

    rebuilt.normalize_active_collapse_direction();

    rebuilt
}

