impl ContextRuntimeStore {
    pub fn is_empty(&self) -> bool {
        self.entry_log.is_empty()
            && self.collapse_commits.is_empty()
            && self.staged_snapshot.is_none()
            && self.session_memory.is_none()
            && self.snip_entries.is_empty()
            && self.microcompact_entries.is_empty()
            && self.preserved_segment.is_none()
            && self.boundary.is_none()
    }
}

